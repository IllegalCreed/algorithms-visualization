const SEARCH_ALIASES: Record<string, string[]> = {
  fenwick: ['bit', 'binary indexed tree'],
  'quick-sort': ['qsort', 'qs', 'kuaipai'],
  'b-tree': ['b tree', 'b plus tree', 'b+ tree'],
  'binary-search': ['bs', 'erfen'],
  'aho-corasick': ['ac automaton', 'ac 自动机'],
  'sieve-of-eratosthenes': ['eratosthenes', 'prime sieve'],
  'pollard-rho': ['rho'],
};

export function normalizeToken(token: string): string {
  return token.trim().toLowerCase();
}

export function pinyinInitials(text: string): string {
  return Array.from(text)
    .map((char) => {
      if (/[\da-z]/i.test(char)) return char.toLowerCase();
      return PINYIN_INITIALS[char] ?? '';
    })
    .join('');
}

function slugAcronym(slug: string): string {
  return slug
    .split('-')
    .filter(Boolean)
    .map((part) => part[0])
    .join('');
}

export function buildSearchTokens(entry: SearchTokenSource): string[] {
  const slugWords = entry.url.replace(/-/g, ' ');
  return Array.from(
    new Set(
      [
        entry.title,
        entry.desc,
        entry.url,
        entry.category,
        slugWords,
        entry.url.replace(/-/g, ''),
        slugAcronym(entry.url),
        pinyinInitials(entry.title),
        pinyinInitials(entry.category),
        ...(SEARCH_ALIASES[entry.url] ?? []),
      ]
        .map(normalizeToken)
        .filter(Boolean),
    ),
  );
}
